const LangSystem = {
  current: 'en',

  init() {
    const saved = localStorage.getItem('carpet_lang') || 'en';
    this.apply(saved);
    this.bindButtons();
  },

  apply(lang) {
    this.current = lang;
    localStorage.setItem('carpet_lang', lang);
    document.documentElement.lang = lang;
    document.body.dir = lang === 'ar' ? 'rtl' : 'ltr';

    document.querySelectorAll('[data-lang-key]').forEach(el => {
      const key = el.getAttribute('data-lang-key');
      const val = translations[lang]?.[key];
      if (val !== undefined) {
        if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
          el.placeholder = val;
        } else {
          el.textContent = val;
        }
      }
    });

    document.querySelectorAll('[data-lang-btn]').forEach(btn => {
      btn.classList.toggle('active', btn.getAttribute('data-lang-btn') === lang);
    });
  },

  t(key) {
    return translations[this.current]?.[key] || translations.en?.[key] || key;
  },

  bindButtons() {
    document.querySelectorAll('[data-lang-btn]').forEach(btn => {
      btn.addEventListener('click', () => this.apply(btn.getAttribute('data-lang-btn')));
    });
  }
};

export { LangSystem, translations };
